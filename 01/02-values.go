package main

import "fmt"

func main() {
	//Concatenate Strings
	fmt.Println("Hello" + " " + "World" + "!")

	// Mathematic Operations

	fmt.Println("Sum of 1 and 2 is ", 1+2)
	fmt.Println("Difference of 7 and 2 is ", 7-2)
	fmt.Println("Multiplication of 2 and 6 is ", 2*6)
	fmt.Println("division of 7 and 4 is ", 7/4)
	fmt.Println("division of 7 and 4 is ", 7.0/4.0)
	fmt.Println("Remainder from 7/4 is ", 7%4)

}
